def main():
    print("Loading font.json...")
    glyphs = load_font()
    existing_codes = set(g["code"] for g in glyphs)
    existing_bmap = build_lookup(glyphs)
    print(f"  Existing glyphs: {len(glyphs)}")

    print("Generating new glyphs...")
    candidates = make_new_glyphs(existing_bmap)

    # Deduplicate: skip codes already present
    added = []
    skipped_existing = []
    for g in candidates:
        if g["code"] in existing_codes:
            skipped_existing.append(g["code"])
        else:
            added.append(g)
            existing_codes.add(g["code"])

    print(f"  Candidates generated: {len(candidates)}")
    print(f"  Already present (skipped): {len(skipped_existing)}")
    print(f"  New glyphs to add: {len(added)}")

    # Merge and sort
    merged = glyphs + added
    merged.sort(key=lambda g: g["code"])

    # Validate bitmaps
    errors = 0
    for g in merged:
        bm_rows = g["bitmap"]
        if len(bm_rows) != 16:
            print(f"  ERROR: U+{g['code']:04X} has {len(bm_rows)} rows!", file=sys.stderr)
            errors += 1
            continue
        for i, row in enumerate(bm_rows):
            if len(row) != 8 or not all(c in "01" for c in row):
                print(f"  ERROR: U+{g['code']:04X} row {i}: {row!r}", file=sys.stderr)
                errors += 1
    if errors:
        print(f"  {errors} validation errors - aborting!", file=sys.stderr)
        sys.exit(1)

    # Write back
    with open(FONT_PATH, "w") as f:
        json.dump(merged, f, indent=2, ensure_ascii=False)
    print(f"  Wrote {len(merged)} glyphs to font.json")

    # Summary per range
    print()
    print("Summary by range:")
    ranges = [
        ("Basic Latin + Latin-1",       0x0000, 0x00FF),
        ("Latin Extended A/B",           0x0100, 0x02FF),
        ("Greek and Coptic",             0x0370, 0x03FF),
        ("Cyrillic",                     0x0400, 0x04FF),
        ("Cyrillic Supplement",          0x0500, 0x057F),
        ("General Punctuation",          0x2000, 0x207F),
        ("Superscripts and Subscripts",  0x2070, 0x209F),
        ("Currency Symbols",             0x20A0, 0x20CF),
        ("Number Forms",                 0x2150, 0x218F),
        ("Arrows",                       0x2190, 0x21FF),
        ("Mathematical Operators",       0x2200, 0x22FF),
        ("Box Drawing + Block Elements", 0x2500, 0x25FF),
        ("Other/Misc",                   0x0000, 0xFFFF),
    ]
    counted = set()
    for name, lo, hi in ranges[:-1]:
        codes = [g["code"] for g in merged if lo <= g["code"] <= hi]
        counted.update(codes)
        new_in_range = [g["code"] for g in added if lo <= g["code"] <= hi]
        print(f"  {name}: {len(codes)} glyphs total ({len(new_in_range)} new)")
    misc = [g["code"] for g in merged if g["code"] not in counted]
    print(f"  Other: {len(misc)} glyphs")
    print()
    print(f"Total glyphs in font.json: {len(merged)}")


if __name__ == "__main__":
    main()
