SCRIPT_DIR = os.path.dirname(os.path.abspath(__file__))
FONT_PATH = os.path.join(SCRIPT_DIR, "font.json")

# ---------------------------------------------------------------------------
# Bitmap helpers
# ---------------------------------------------------------------------------

BLANK_GLYPH = ["00000000"] * 16

def bm(rows):
    """Build a 16-row bitmap list from a list of strings, padding as needed."""
    assert len(rows) == 16, f"Expected 16 rows, got {len(rows)}"
    result = []
    for r in rows:
        assert len(r) == 8 and all(c in "01" for c in r), f"Bad row: {r!r}"
        result.append(r)
    return result

def B(s):
    """Convert an 8-char string of 0/1 to itself (passthrough, for readability)."""
    return s

# ---------------------------------------------------------------------------
# Uppercase caps height helper: rows 2-13 active (like Latin uppercase)
# Lowercase x-height helper: rows 5-11 active (like Latin lowercase)
# ---------------------------------------------------------------------------

def uc(interior):
    """Wrap 10 interior rows in uppercase template (rows 0-1 blank, rows 2-11 content, rows 12-15 blank)."""
    assert len(interior) == 10
    return ["00000000", "00000000"] + interior + ["00000000", "00000000", "00000000", "00000000"]

def lc(interior, rows_before=5, rows_after=None):
    """Wrap 7 interior rows in lowercase template."""
    assert len(interior) == 7
    before = ["00000000"] * rows_before
    after_count = 16 - rows_before - 7
    after = ["00000000"] * after_count
    return before + interior + after

# ---------------------------------------------------------------------------
# Load existing font
# ---------------------------------------------------------------------------

def load_font():
    with open(FONT_PATH) as f:
        return json.load(f)

def build_lookup(glyphs):
    return {g["code"]: g["bitmap"] for g in glyphs}

