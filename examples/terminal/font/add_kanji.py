"""Add common Kanji glyphs to font.json + generate kanji_lut.mem.

Kanji are 16x16 wide characters stored in internal range U+4000-40FF.
(NOT Unicode PUA U+E000+ because that has bit 15 set, conflicting with
the right-half flag used by the terminal's wide char system.)

The terminal hardware uses a lookup ROM to remap CJK codepoints
(U+4E00-9FFF) to internal slots at receive time.

This script:
1. Defines ~240 common Kanji (Grade 1-3 Joyo) as 16x16 bitmaps
2. Assigns each an internal slot (U+4000 + index)
3. Adds the internally-coded glyphs to font.json
4. Generates kanji_lut.mem (256 entries of 16-bit Unicode codepoints, sorted)
   for the binary search ROM in terminal.jz
"""

