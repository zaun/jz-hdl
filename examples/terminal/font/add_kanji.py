def main():
    with open(FONT_PATH) as f:
        glyphs = json.load(f)

    existing_codes = {g["code"] for g in glyphs}

    # Sort Kanji by Unicode codepoint for lookup ROM
    kanji_sorted = sorted(KANJI, key=lambda k: k[0])

    if len(kanji_sorted) > 256:
        print(f"Warning: {len(kanji_sorted)} Kanji exceeds 256-slot limit, truncating", file=sys.stderr)
        kanji_sorted = kanji_sorted[:256]

    # Deduplicate (some Kanji like 立 may appear twice)
    seen = set()
    kanji_dedup = []
    for k in kanji_sorted:
        if k[0] not in seen:
            seen.add(k[0])
            kanji_dedup.append(k)
    kanji_sorted = kanji_dedup

    print(f"Processing {len(kanji_sorted)} Kanji...")

    # Assign PUA slots: U+E000 + index
    added = []
    unicode_to_slot = {}

    for idx, (unicode_cp, char, bitmap) in enumerate(kanji_sorted):
        pua_code = 0x4000 + idx
        unicode_to_slot[unicode_cp] = idx

        if pua_code in existing_codes:
            continue

        # Validate bitmap
        assert len(bitmap) == 16, f"U+{unicode_cp:04X} ({char}) has {len(bitmap)} rows"
        for row in bitmap:
            assert len(row) == 16 and all(c in "01" for c in row), \
                f"U+{unicode_cp:04X} ({char}) bad wide row"

        added.append({
            "code": pua_code,
            "char": char,
            "bitmap": bitmap,
            "_unicode": unicode_cp  # metadata for reference
        })

    if not added:
        print("All Kanji glyphs already present.")
    else:
        # Remove metadata before saving
        for g in added:
            del g["_unicode"]

        # Merge and sort
        merged = glyphs + added
        merged.sort(key=lambda g: g["code"])

        with open(FONT_PATH, "w") as f:
            json.dump(merged, f, indent=2, ensure_ascii=False)

        print(f"Added {len(added)} Kanji glyphs (U+4000-40{len(kanji_sorted)-1:02X})")
        print(f"Total glyphs in font.json: {len(merged)}")

    # Generate kanji_lut.mem (256 entries, 16-bit Unicode codepoints sorted)
    os.makedirs(OUT_DIR, exist_ok=True)
    lut_path = os.path.join(OUT_DIR, "kanji_lut.mem")
    with open(lut_path, "w") as f:
        for idx in range(256):
            if idx < len(kanji_sorted):
                unicode_cp = kanji_sorted[idx][0]
                f.write(f"{unicode_cp:016b}\n")
            else:
                f.write("1111111111111111\n")  # sentinel (no valid CJK at U+FFFF)

    print(f"Generated {lut_path} ({len(kanji_sorted)} entries + padding)")

    # Also generate a human-readable mapping file
    map_path = os.path.join(OUT_DIR, "kanji_map.txt")
    with open(map_path, "w") as f:
        f.write("# Kanji lookup table: slot -> Unicode -> PUA -> character\n")
        f.write(f"# {len(kanji_sorted)} entries\n\n")
        for idx, (unicode_cp, char, _) in enumerate(kanji_sorted):
            pua = 0x4000 + idx
            f.write(f"slot {idx:3d}: U+{unicode_cp:04X} -> PUA U+{pua:04X}  {char}\n")

    print(f"Generated {map_path}")


if __name__ == "__main__":
    main()
