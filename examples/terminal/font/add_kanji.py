def W(hex_str):
    """Convert space-separated 4-digit hex row values to 16-char binary strings."""
    parts = hex_str.split()
    assert len(parts) == 16, f"Expected 16 hex values, got {len(parts)}"
    result = []
    for h in parts:
        val = int(h, 16)
        result.append(format(val, '016b'))
    return result


